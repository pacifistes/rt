#include <stdio.h>

int	ft_sizeint(long long n)
{
	int size;

	size = 1;
	if (n < 0)
	{
		size++;
		n = -n;
	}
	while (n > 9)
	{
		n = n / 10;
		size++;
	}
	return (size);
}
