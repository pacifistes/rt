#include "raystruct.h"

static double	fade(double t)
{
	return (t * t * t * (t * (t * 6 - 15) + 10));
}

static double	lerp(double t, double a, double b)
{
	return (a + t * (b - a));
}

static double	grad(int hash, double x, double y, double z)
{
	int		h;
	double	u;
	double	v;

	h = hash & 15;
	u = h < 8 || h == 12 || h == 13 ? x : y;
	v = h < 4 || h == 12 || h == 13 ? y : z;
	return ((h & 1) == 0 ? u : -u) + ((h & 2) == 0 ? v : -v);
}

double			resolve_perlin(double *d, int *i, int *p)
{
	d[3] = fade(d[0]);
	d[4] = fade(d[1]);
	d[5] = fade(d[2]);
	i[3] = p[i[0]] + i[1];
	i[4] = p[i[3]] + i[2];
	i[5] = p[i[3] + 1] + i[2];
	i[6] = p[i[0] + 1] + i[1];
	i[7] = p[i[6]] + i[2];
	i[8] = p[i[6] + 1] + i[2];
	return (lerp(d[5], lerp(d[4], lerp(d[3], grad(p[i[4]], d[0], d[1], d[2]),
	grad(p[i[7]], d[0] - 1, d[1], d[2])), lerp(d[3], grad(p[i[5]],
		d[0], d[1] - 1, d[2]), grad(p[i[8]], d[0] - 1, d[1] - 1, d[2]))),
	lerp(d[4], lerp(d[3], grad(p[i[4] + 1], d[0], d[1], d[2] - 1),
		grad(p[i[7] + 1], d[0] - 1, d[1], d[2] - 1)), lerp(d[3],
		grad(p[i[5] + 1], d[0], d[1] - 1, d[2] - 1),
	grad(p[i[8] + 1], d[0] - 1, d[1] - 1, d[2] - 1)))));
}

